    public void loadData() {
        connection = Connector.ConnectDb();
        String sql = "select id,dName,pName,room from appointment";
        try {
            ps = connection.prepareStatement(sql);
            rs = ps.executeQuery();
            Object columnData[] = new Object[4];
            while (rs.next()) {
                columnData[0] = rs.getString("id");
                columnData[1] = rs.getString("dName");
                columnData[2] = rs.getString("pName");
                columnData[3] = rs.getString("room");
                
                defaultTableModel.addRow(columnData);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

